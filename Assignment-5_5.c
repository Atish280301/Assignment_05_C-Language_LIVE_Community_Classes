//5. Write a program to calculate the area of a rectangle
#include<stdio.h>
#include<conio.h>

int main()
{
	int length, width, area;
	
	printf("Enter Length Of The Rectangle : \n");
	scanf("%d",&length);
	
	printf("Enter Width Of The Rectangle : \n");
	scanf("%d",&width);
	
	area = length * width;
	
	printf("Area Of Rectangle : %d",area);
	
	getch();
}
